package to;

public class Trim {

	public static void main(String[] args) {
		
		String str1="     Example of the Trim Method     ";
		
		System.out.println("without trim printout:"+str1);  //without trim
		
		System.out.println("trim output:"+str1.trim()+" HELLO TRIM ");  // with trim
		System.out.println(str1.trim());

	}

}
